package App.BasePage;


public class mathAplication {

    public int adding(int numb1, int numb2){
        return numb1 + numb2;
    }
    public int sub(int numb1, int numb2){
        return numb1 - numb2;
    }
    public int multy(int numb1, int numb2){
        return numb1 * numb2;
    }
    public int devis(int numb1, int numb2){
        return numb1 / numb2;
    }

    public static String nameUser(String name){
        return name;
    }
}
